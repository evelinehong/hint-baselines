def train(model, dataloader, optimizer, criterion, clip):
    model.train()
    
    epoch_loss = 0
    
    for sample in tqdm(dataloader):
        
        src = sample['img_seq']
        trg = sample['label_seq']
        src_mask = sample['mask']
        src = src.to(device)
        src_mask = src_mask.to(device)
        trg = trg.to(device)
        optimizer.zero_grad()
        
        output = model(src, trg[:,:-1], src_mask)
        
        #output = [batch size, trg len , output dim]
            #trg = [batch size, trg len]

        output_dim = output.shape[-1]
        
        output = output.contiguous().view(-1, output_dim)
        trg = trg[:,1:].contiguous().view(-1)
        
        #trg = [(trg len - 1) * batch size]
        #output = [(trg len - 1) * batch size, output dim]
        
        loss = criterion(output, trg)
        
        loss.backward()
        
        torch.nn.utils.clip_grad_norm_(model.parameters(), clip)
        
        optimizer.step()
        
        epoch_loss += loss.item()
        
    return epoch_loss / len(dataloader)

def evaluate(model, dataloader):
    
    model.eval()
    
    epoch_acc = 0
    
    len_samples = 0
    with torch.no_grad():
    
        for sample in tqdm(dataloader):
            src = sample['img_seq']
            trg = sample['label_seq']
            src_mask = sample['mask']
            src = src.to(device)
            src_mask = src_mask.to(device)
            trg = trg.to(device)
            len_samples += trg.shape[0]
            
            output = model(src, trg[:,:-1], src_mask) 

            output_dim = output.shape[-1]
            
            preds = torch.argmax(output, -1)
            preds = preds.detach().cpu()
            trg = trg[:,1:].detach().cpu()
            acc = torch.all(torch.eq(preds, trg), dim = 1)
            epoch_acc += np.sum(acc.numpy())
        
    return epoch_acc / len_samples

def epoch_time(start_time, end_time):
    elapsed_time = end_time - start_time
    elapsed_mins = int(elapsed_time / 60)
    elapsed_secs = int(elapsed_time - (elapsed_mins * 60))
    return elapsed_mins, elapsed_secs

